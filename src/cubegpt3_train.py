def to_ordinal_targets(y, num_classes=21):
    # y: (B)
    # returns: (B, num_classes - 1)
    # Note: We usually use num_classes - 1 because the 0th state is implicit
    # or you can use num_classes if you want a column for every step.

    # Create a range tensor: [0, 1, 2, ..., 20]
    levels = torch.arange(num_classes).to(device)

    # Compare: (B, 1) > (1, 21) -> (B, 21)
    # This results in 1s where the index <= truth, 0s otherwise
    ordinal_y = (y.view(-1, 1) >= levels).float()
    return ordinal_y

def test_batch(X : torch.Tensor, y : torch.Tensor, model : nn.Module, loss_fn : nn.Module, convert_float=False):
    pred = model(X)
    if convert_float:
        loss = loss_fn(pred, y.float())
    else:
        loss = loss_fn(pred, y)
    total_loss = loss.item() * len(y)
    return total_loss

def train_batch(X : torch.Tensor, y : torch.Tensor, model : nn.Module, loss_fn : nn.Module, optimizer : torch.optim.Optimizer, convert_float=False):
    # Forward pass
    pred = model(X)
    if convert_float:
        loss = loss_fn(pred, y.float())
    else:
        loss = loss_fn(pred, y)
    total_loss = loss.item() * len(y)

    # Backward pass
    optimizer.zero_grad()
    loss.backward()
    optimizer.step()

    # Return total loss
    return total_loss

def get_ord_avgs(model, batch):
    logits = model(batch)
    probs = nn.Sigmoid()(logits)
    avgs = torch.sum(probs, dim=1) - 1
    return avgs

