class MoEBlock(nn.Module):
    """
    DeepSeek-V3-style Mixture-of-Experts FFN with auxiliary-loss-free
    load balancing.

    Routing, per token:
        1. affinity   s_i = sigmoid(w_i . x)          (sigmoid, no bias)
        2. selection  top-k over  s_i + b_i           (bias used ONLY here)
        3. gate value g_i = s_i for the selected experts, normalized over the
                      k selected experts, optionally * routed_scaling_factor

    The per-expert bias b_i is not learned by gradient descent. After each
    optimizer step you call `update_expert_bias()`, which nudges each bias by
    +/- gamma depending on whether the expert was under- or over-loaded in the
    last forward. That is the entire load-balancing mechanism: no aux loss in
    the main objective, no gradient through the bias.
    """

    def __init__(
        self,
        d_model,
        d_ffwd,
        n_experts,
        n_active,
        bias=True,
        gated=True,
        activation=nn.functional.gelu,
        bias_update_speed=1e-3,    # gamma
        routed_scaling_factor=1.0, # V3 uses 2.5; 1.0 is fine for a small model
    ):
        super().__init__()
        assert n_active <= n_experts

        self.n_experts = n_experts
        self.n_active = n_active
        self.bias_update_speed = bias_update_speed
        self.routed_scaling_factor = routed_scaling_factor

        if gated:
            self.experts = nn.ModuleList([
                MLPBlockGated(d_model, d_ffwd, bias=bias)
                for _ in range(n_experts)
            ])
        else:
            self.experts = nn.ModuleList([
                MLPBlock(d_model, d_ffwd, bias=bias, activation=activation)
                for _ in range(n_experts)
            ])

        # router: centroid affinity logits, no bias term
        self.gate = nn.Linear(d_model, n_experts, bias=False)

        # additive routing bias b_i -- a buffer, updated by hand, no grad
        self.register_buffer("expert_bias", torch.zeros(n_experts))
        # token load from the most recent forward, consumed by the bias update
        self.register_buffer("tokens_per_expert", torch.zeros(n_experts))

    def forward(self, x):
        # x: (B, T, d_model) -> (N, d_model)
        B, T, d_model = x.shape
        x = x.reshape(-1, d_model)

        # 1. affinity scores (sigmoid -- V3 changed this from V2's softmax)
        scores = self.gate(x).sigmoid()                        # (N, n_experts)

        # 2. top-k selection on the BIASED scores
        biased = scores + self.expert_bias                     # (N, n_experts)
        topk_idx = biased.topk(self.n_active, dim=-1).indices  # (N, n_active)

        # 3. gate values from the UNBIASED scores, normalized over selected
        topk_scores = scores.gather(-1, topk_idx)              # (N, n_active)

        if self.n_active == 1:
            topk_weights = topk_scores
        else:
            topk_weights = topk_scores / (topk_scores.sum(-1, keepdim=True) + 1e-20)

        topk_weights = topk_weights * self.routed_scaling_factor

        # dispatch: run each expert on the tokens routed to it
        out = torch.zeros_like(x)
        load = torch.zeros(self.n_experts, device=x.device)
        for e, expert in enumerate(self.experts):
            tok, slot = (topk_idx == e).nonzero(as_tuple=True)
            if tok.numel() == 0:
                continue
            w = topk_weights[tok, slot].unsqueeze(-1)          # (n_assigned, 1)
            out.index_add_(0, tok, expert(x[tok]) * w)
            load[e] = tok.numel()

        if self.training:
            self.tokens_per_expert.copy_(load.detach())

        return out.view(B, T, d_model)

    @torch.no_grad()
    def update_expert_bias(self):
        """Call once per step, AFTER optimizer.step()."""
        load = self.tokens_per_expert
        err = load.mean() - load            # >0 under-loaded -> raise bias
        self.expert_bias += self.bias_update_speed * torch.sign(err)

