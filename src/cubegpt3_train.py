def get_colors_str_by_pos(cube_string, position):
    return "".join([cube_string[i] for i in pos_face_idx[position]])

def string_to_tokens(cube_string):
    tokens_tensor = torch.zeros((20, 2), dtype=torch.int32)

    orien_idx_to_token = {
        0: 0,
        3: 1,
        4: 2,
        1: 0,
        2: 1,
        5: 2,
    }

    edge_orientations = 2
    corner_orientations = 3

    for pos, position_idx in pos_idx.items():
        color_str = get_colors_str_by_pos(cube_string, pos)
        is_edge = len(color_str) == 2

        piece_idx = color_str_tensor_idx[color_str]
        orientation_idx = color_str_orientation[color_str]

        if is_edge:
            placement_token = position_idx * edge_orientations + orientation_idx
        else:
            orientation_idx = orien_idx_to_token[orientation_idx]
            placement_token = position_idx * corner_orientations + orientation_idx
            placement_token += 24

        tokens_tensor[piece_idx][0] = piece_idx # redundant, but makes code more consistent.
        tokens_tensor[piece_idx][1] = placement_token

    return tokens_tensor

def string_to_tensor(cube_string):
    cube_tensor = torch.zeros((20, 14), dtype=torch.float32)

    for pos, pos_value in pos_idx.items():
        color_str = get_colors_str_by_pos(cube_string, pos)
        is_edge = len(color_str) == 2

        tensor_idx = color_str_tensor_idx[color_str]
        orientation_value = color_str_orientation[color_str]

        cube_tensor[tensor_idx][pos_value] = 1
        if is_edge:
            cube_tensor[tensor_idx][12 + orientation_value] = 1
        else:
            cube_tensor[tensor_idx][8 + orientation_value] = 1

    return cube_tensor

def rotate_string(cube_str, move=None):
    """Fast rotate function for cube strings."""
    return "".join([cube_str[i] for i in move_map[move]])

class CubeState:
    # moves[move_class][move_type]
    # move_class --> U, U', U2, D, D', D2
    # move_type --> U, U', U2
    # when scrambling:
    # maximum 2 moves with same move_class in a row
    # maximum 1 move with same move_class, move_type in a row
    moves = [
        [["U", "U'", "U2"], ["D", "D'", "D2"]],
        [["L", "L'", "L2"], ["R", "R'", "R2"]],
        [["F", "F'", "F2"], ["B", "B'", "B2"]],
             ]

    def __init__(self, cube_string : str = solved_string,
                 prev_state = None,
                 prev_move = None,
                 prev_move_class = None,
                 prev_move_type = None
                 ):
        self.cube_string = cube_string
        self.prev_state = prev_state
        self.prev_move = prev_move
        self.prev_move_class = prev_move_class
        self.prev_move_type = prev_move_type

        self.prev2_move_class = None
        if prev_state:
            self.prev2_move_class = prev_state.prev_move_class

    def forget(self):
        """Forget this Cube's history (for fair solving)."""
        self.prev_state = None
        self.prev_move = None
        self.prev_move_class = None
        self.prev_move_type = None
        self.prev2_move_class = None

    def get_tensor(self):
        return string_to_tensor(self.cube_string)

    def get_tokens(self):
        return string_to_tokens(self.cube_string)

    def get_allowed_moves(self):
        # (move_class, move_type, move_index)
        for move_class in range(len(CubeState.moves)):
            # Skip if this would be the 3rd consecutive move from the same move class
            if (move_class == self.prev_move_class and
                move_class == self.prev2_move_class):
                continue

            for move_type in range(len(CubeState.moves[move_class])):
                # Skip if this would be the 2nd consecutive move from the same move type
                if (move_class == self.prev_move_class and
                    move_type == self.prev_move_type):
                    continue

                for move_index in range(len(CubeState.moves[move_class][move_type])):
                    yield (move_class, move_type, move_index)

    def get_child(self, move_class, move_type, move_index):
        """Get child state with given move_class, move_type, and move_index"""
        move = CubeState.moves[move_class][move_type][move_index]
        return CubeState(
            cube_string=rotate_string(self.cube_string, move),
            prev_state=self,
            prev_move=move,
            prev_move_class=move_class,
            prev_move_type=move_type
        )

    def get_random_child(self):
        allowed_moves = list(self.get_allowed_moves())
        move_class, move_type, move_index = allowed_moves[np.random.randint(len(allowed_moves))]
        return self.get_child(move_class, move_type, move_index)

    def get_children(self):
        """Generate all valid child states following the move constraints."""
        children = []
        for (move_class, move_type, move_index) in self.get_allowed_moves():
            children.append(self.get_child(move_class, move_type, move_index))
        return children

    def get_child_dict(self):
        """Generate a dictionary with all child constraints. Does not consider move constraints."""
        children = {}
        for move_class in range(len(CubeState.moves)):
            for move_type in range(len(CubeState.moves[move_class])):
                for move_index in range(len(CubeState.moves[move_class][move_type])):
                    move = CubeState.moves[move_class][move_type][move_index]
                    children[move] = self.get_child(move_class, move_type, move_index)
        return children

INCLUDE_MIRRORS = True   # True -> 48 symmetries, False -> 24 (rotations only)

# ----------------------------------------------------------------------------
# STEP 0 — Basic facts about the 54-character cube string.
# Facelets 0-8 are face 0, facelets 9-17 are face 1, and so on.
# ----------------------------------------------------------------------------
NUM_FACES = 6
NUM_FACELETS = 54

def face_of_facelet(facelet):
    return facelet // 9

CENTER_FACELET = [9 * face + 4 for face in range(NUM_FACES)]
SOLVED_COLOR = [solved_string[CENTER_FACELET[face]] for face in range(NUM_FACES)]
# SOLVED_COLOR == ['W', 'O', 'G', 'R', 'B', 'Y']

# ----------------------------------------------------------------------------
# STEP 1 — Which direction does each face point?
#
# Cubie positions are (x, y, z) coordinates in {0,1,2}^3. A sticker lies on one
# of the six outer planes of the cube; we describe a plane by its outward
# normal, e.g. (-1, 0, 0) means the x=0 plane. To find each face's normal,
# intersect the candidate planes over every cubie carrying a sticker of that
# face — only the true normal survives.
# ----------------------------------------------------------------------------
def outward_normals(position):
    """The outer planes touched by the cubie at this position."""
    normals = set()
    for axis in range(3):
        if position[axis] == 0:
            normal = [0, 0, 0]; normal[axis] = -1
            normals.add(tuple(normal))
        if position[axis] == 2:
            normal = [0, 0, 0]; normal[axis] = +1
            normals.add(tuple(normal))
    return normals

candidate_normals = [None] * NUM_FACES
for position, facelets in pos_face_idx.items():
    for facelet in facelets:
        face = face_of_facelet(facelet)
        if candidate_normals[face] is None:
            candidate_normals[face] = outward_normals(position)
        else:
            candidate_normals[face] &= outward_normals(position)

assert all(len(candidates) == 1 for candidates in candidate_normals)
FACE_NORMAL = [candidates.pop() for candidates in candidate_normals]

# ----------------------------------------------------------------------------
# STEP 2 — A coordinate system for every sticker.
#
# Every sticker is uniquely identified by (cubie position, face normal).
# Centers get pseudo-positions like (0, 1, 1) so all 54 facelets are covered.
# ----------------------------------------------------------------------------
FACELET_AT = {}   # (position, normal) -> facelet index
for position, facelets in pos_face_idx.items():
    for facelet in facelets:
        normal = FACE_NORMAL[face_of_facelet(facelet)]
        FACELET_AT[(tuple(position), normal)] = facelet

for face in range(NUM_FACES):
    normal = FACE_NORMAL[face]
    center_position = (1 + normal[0], 1 + normal[1], 1 + normal[2])
    FACELET_AT[(center_position, normal)] = CENTER_FACELET[face]

assert len(FACELET_AT) == NUM_FACELETS

# ----------------------------------------------------------------------------
# STEP 3 — The whole-cube symmetries as 3x3 matrices.
#
# The rotations of a cube are exactly the signed permutation matrices with
# determinant +1 (24 of them). Determinant -1 adds the mirror images (48
# total). Matrices act on positions about the cube's center (1, 1, 1).
# ----------------------------------------------------------------------------
symmetry_matrices = []
for axis_permutation in permutations(range(3)):
    for signs in product((+1, -1), repeat=3):
        matrix = np.zeros((3, 3), dtype=int)
        for row in range(3):
            matrix[row, axis_permutation[row]] = signs[row]
        is_rotation = round(np.linalg.det(matrix)) == 1
        if is_rotation or INCLUDE_MIRRORS:
            symmetry_matrices.append(matrix)

def transform_position(matrix, position):
    centered = np.array(position) - 1
    return tuple(matrix @ centered + 1)

def transform_normal(matrix, normal):
    return tuple(matrix @ np.array(normal))

# ----------------------------------------------------------------------------
# STEP 4 — Compile each matrix into fast string operations.
#
# Each symmetry becomes:
#   gather  : sticker permutation, same convention as move_map
#             (new_string[i] = old_string[gather[i]])
#   recolor : str.translate table that repaints stickers so centers are
#             standard again. It depends only on the symmetry, not the state,
#             because moves never displace centers.
# ----------------------------------------------------------------------------
SYMMETRIES = []
for matrix in symmetry_matrices:
    gather = [None] * NUM_FACELETS
    for (position, normal), facelet in FACELET_AT.items():
        new_position = transform_position(matrix, position)
        new_normal = transform_normal(matrix, normal)
        new_facelet = FACELET_AT[(new_position, new_normal)]
        gather[new_facelet] = facelet

    rotated_solved = "".join(solved_string[gather[i]] for i in range(NUM_FACELETS))
    recolor_map = {}
    for face in range(NUM_FACES):
        color_after_rotation = rotated_solved[CENTER_FACELET[face]]
        recolor_map[color_after_rotation] = SOLVED_COLOR[face]
    recolor = str.maketrans(recolor_map)

    SYMMETRIES.append((gather, recolor))

assert len(SYMMETRIES) == (48 if INCLUDE_MIRRORS else 24)

# ----------------------------------------------------------------------------
# STEP 5 — Apply a symmetry; canonicalize over all of them.
# ----------------------------------------------------------------------------
def apply_symmetry(cube_string, gather, recolor):
    permuted = "".join(cube_string[gather[i]] for i in range(NUM_FACELETS))
    return permuted.translate(recolor)

def canonicalize(cube_string):
    variants = [apply_symmetry(cube_string, gather, recolor)
                for gather, recolor in SYMMETRIES]
    return min(variants)

# ----------------------------------------------------------------------------
# STEP 6 — Optional extra factor of 2: distance(s) == distance(s_inverse).
#
# To invert a state: every sticker's color tells you which face it belongs to,
# so summing the face normals of a cubie's colors gives its home position.
# Send each sticker home, and record at the home facelet the color of the slot
# the sticker came from.
# ----------------------------------------------------------------------------
def invert_string(cube_string):
    result = [None] * NUM_FACELETS
    for face in range(NUM_FACES):
        result[CENTER_FACELET[face]] = SOLVED_COLOR[face]

    for position, facelets in pos_face_idx.items():
        home_position = np.array([1, 1, 1])
        for facelet in facelets:
            color = cube_string[facelet]
            home_position += np.array(FACE_NORMAL[SOLVED_COLOR.index(color)])
        home_position = tuple(home_position)

        for facelet in facelets:
            color = cube_string[facelet]
            home_facelet = FACELET_AT[(home_position,
                                       FACE_NORMAL[SOLVED_COLOR.index(color)])]
            result[home_facelet] = SOLVED_COLOR[face_of_facelet(facelet)]

    return "".join(result)

def canonicalize_full(cube_string):
    return min(canonicalize(cube_string), canonicalize(invert_string(cube_string)))

# ----------------------------------------------------------------------------
# STEP 7 — Self-tests. Run these once; they catch every plausible bug.
